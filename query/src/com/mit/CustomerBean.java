package com.mit;

public class CustomerBean {
	int cid;
	String cname;
	String crole;
	public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public String getCrole() {
		return crole;
	}
	public void setCrole(String crole) {
		this.crole = crole;
	}

}
